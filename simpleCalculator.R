new.add <- function(number1,number2){
  
  result <- number1 + number2
  print(result)
}

new.subtract <- function(number1,number2){
  
  result <- number1 - number2
  print(result)
}

new.multiply <- function(number1,number2){
  
  result <- number1 * number2
  print(result)
}

new.divide <- function(number1,number2){
  
  result <- number1 / number2
  print(result)
}

new.add(6,5)
new.subtract(6,5)
new.multiply(6,5)
new.divide(6,2)